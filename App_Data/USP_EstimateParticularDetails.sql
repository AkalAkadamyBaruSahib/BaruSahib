ALTER procedure [dbo].[USP_EstimateParticularDetails]    
(    
@EstId as int    
)    
as    
begin    
SELECT     MaterialType.MatTypeName,
           PurchaseSource.PSName, Unit.UnitName, EstimateAndMaterialOthersRelations.Sno, EstimateAndMaterialOthersRelations.EstId, 
           EstimateAndMaterialOthersRelations.Qty, EstimateAndMaterialOthersRelations.Rate, EstimateAndMaterialOthersRelations.Amount, 
           EstimateAndMaterialOthersRelations.Remark, EstimateAndMaterialOthersRelations.Active, CONVERT(nvarchar(20), EstimateAndMaterialOthersRelations.CreatedOn, 
           107) AS CreatedOn, EstimateAndMaterialOthersRelations.CreatedBy, EstimateAndMaterialOthersRelations.ModifyOn, EstimateAndMaterialOthersRelations.ModifyBy, 
           Material.MatName, EstimateAndMaterialOthersRelations.MatId,MaterialType.MatTypeName
FROM       EstimateAndMaterialOthersRelations INNER JOIN
           MaterialType ON EstimateAndMaterialOthersRelations.MatTypeId = MaterialType.MatTypeId INNER JOIN












                      PurchaseSource ON EstimateAndMaterialOthersRelations.PSId = PurchaseSource.PSId INNER JOIN































































































































                      Unit ON EstimateAndMaterialOthersRelations.UnitId = Unit.UnitId INNER JOIN































































































































                      Material ON EstimateAndMaterialOthersRelations.MatId = Material.MatId where EstimateAndMaterialOthersRelations.EstId=@EstId  































































































































                          































































































































                          































































































































                      end
